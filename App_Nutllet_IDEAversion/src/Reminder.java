//package Merge;

/**
 * Represents a financial reminder in the Nutllet application.
 * This class stores information about a reminder including its name, type,
 * amount range, progress tracking, and associated remarks.
 *
 * @author Simeng Lyu
 * @version final
 */
public class Reminder {
    public String name;
    public String type;
    public double minAmount;
    public double maxAmount;
    public String remark;
    public double progress;
    public String progressText;
} 